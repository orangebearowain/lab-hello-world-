numb = input("Please input a number: ")
numb2 = input("Please input a second number: ")
oper = input("Please input an operator: ")

if oper == "+":
    print(int(numb) + int(numb2))
elif oper == "-":
    print(int(numb) - int(numb2))
elif oper == "*":
    print(int(numb) * int(numb2))
elif oper == "/":
    print(int(numb) / int(numb2))
